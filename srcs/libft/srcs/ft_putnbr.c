#include "libft.h"

void	ft_putnbr(int n)
{
	int		div;
	char	nbr;

	div = 1000000000;
	while ((n / div) == 0 && n != 0)
		div /= 10;
	if (n < 0 && write(1, "-", 1))
		div *= -1;
	if (n == 0)
		div = 1;
	while (div != 0)
	{
		nbr = (n / div) + 48;
		write(1, &nbr, 1);
		n %= div;
		div /= 10;
	}
}
