#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	int		i;
	size_t	z;

	z = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[z] != '\0' && z < n)
		s1[i++] = s2[z++];
	s1[i] = '\0';
	return (s1);
}
