#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;

	if (s1 != NULL && s2 != NULL)
	{
		i = 0;
		while ((s1[i] != '\0' || s2[i] != '\0') && (s1[i] == s2[i]))
			i++;
		return (s1[i] == '\0' && s2[i] == '\0' ? 1 : 0);
	}
	return (0);
}
